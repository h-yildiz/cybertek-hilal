package classes.day11;

public class whileLoop {
	
	public static void main(String[] args) {
		
		int i=0;
		while(i<10) {
			System.out.println(i);
			i++;
		}
		
		int numberOfStudents=1;
		
		while (numberOfStudents<=10) {
			
			System.out.println("Student " + numberOfStudents);
			
			numberOfStudents++;
		}
		
	}

}
