package classes.day9;

public class OrLogicWithSwitch {
	public static void main(String[] args) {
		
		char grade='D';
		
		switch (grade) {
		case 'A':
			System.out.println("Pass");
			break;
		case 'B':
			System.out.println("Pass");
			break;
		case 'C':
			System.out.println("Pass");
			break;
		case 'D':
			System.out.println("Fail");
			break;
		case 'E':
			System.out.println("Fail");
			break;
		}
	}

}
